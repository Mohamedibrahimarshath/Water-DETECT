L_blue = rescale(L_blue);
idx_light_blue = SVM(nonzeros(L_blue));
blue_idx = find(mask3);
mask_dark_blue = mask3;
mask_dark_blue(blue_idx(idx_light_blue)) = 0;
blue_nuclei = he .* uint8(mask_dark_blue);
axes(handles.axes3); imshow(blue_nuclei);title('SVM Image');
aa=mean2(blue_nuclei);
